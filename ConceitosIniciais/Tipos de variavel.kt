    println("Int MAX ${Int.MAX_VALUE} - MIN ${Int.MAX_VALUE}")
    println("Short MAX ${Short.MAX_VALUE} - MIN ${Short.MAX_VALUE}")
    println("Byte MAX ${Byte.MAX_VALUE} - MIN ${Byte.MAX_VALUE}")
