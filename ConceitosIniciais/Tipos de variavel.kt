    var c: Char = 'a'
    var s: String = "aasfasf afafasf afa fafasfa"
    var b: Boolean = true

    var double: Double = 10.0
    var float: Float = 10f
    var long: Long = 10L
    var int: Int = 10
    var short: Short = 10
    var byte: Byte = 10

    println("Double MAX ${Double.MAX_VALUE} - MIN ${Double.MAX_VALUE}")
